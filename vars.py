# Запустить это - python vars.py

# Сложение
apples = 1
bananas = 2
print("Apples:", apples)
print("Bananas:", bananas)

fruits = apples + bananas
print("Fruits:", fruits)

# Умножение
width = 10
height = 25
print("Width:", width)
print("Height:", height)

area = width * height
print("Area:", area)

# Конкатенация = сложение строк
alex = "Alex"
print(alex)

likes_python = " likes Python"
print(likes_python)

alex_likes_python = alex + likes_python  # = "Alex likes Python"
print(alex_likes_python)

# Остальные операции
x = 2
y = 3
print("x:", x)
print("y:", y)
print("y / x =", y / x)  # деление
print("y // x =", y // x)  # целочисленное деление
print("x ** 2 =", x ** 2)  # возведение в степень
print("y % x =", y % x)  # взятие остатка
print("-x =", -x)  # смена знака

# Ввод данных
name = input("Hi! What is you name? ")
print("Hello, " + name + "!")